int		count_words(char const *s)
{
	int		i;
	int		count;

	i = 0;
	count = 0;
	while (s[i])
	{
		if (s[i] != ' ')
		{
			if (s[i + 1] == ' ' || s[i + 1] == 0)
				count++;
			i++;
		}
		else
			i++;
	}
	return (count);
}
